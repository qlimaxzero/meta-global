    //redis乐观锁
    public void watchLock(String key, int count, boolean isDiscount) {
        redisTemplate.watch(key);

        int value = redisTemplate.opsForValue().get(key) == null ? 0 : (int) redisTemplate.opsForValue().get(key);
        if (value > 0) {
            SessionCallback sessionCallback = new SessionCallback() {
                @Override
                public Object execute(RedisOperations operations) throws DataAccessException {
                    operations.multi();
                    if (isDiscount) {
                        operations.opsForValue().set(key, value - count);
                    } else {
                        operations.opsForValue().set(key, value + count);
                    }
                    return operations.exec();
                }
            };
            redisTemplate.execute(sessionCallback);

        } else {
            throw new CloudException(ExceptionConstant.库存不足);
        }

    }

    public <V> Long zadd(String key, Map<V, BigDecimal> map) {
        Set<DefaultTypedTuple> collect = map.entrySet().stream()
                .map(x -> new DefaultTypedTuple(x.getKey(), x.getValue().doubleValue()))
                .collect(Collectors.toSet());
        return redisTemplate.opsForZSet().add(key, collect);
    }

