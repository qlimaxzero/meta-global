    /**
     * 删除缓存
     *
     * @param key 可以传一个值 或多个
     */
    public boolean delete(String... key) {
        log.info("redis删除数据，key：{}，", key);
        if (key != null && key.length > 0) {
            if (key.length == 1) {
                return redisTemplate.delete(key[0]);
            } else {
                return redisTemplate.delete(CollectionUtils.arrayToList(key)) > 0;
            }
        }

        return false;
    }

